#include <stdio.h>

/**
 * add - adds
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subs
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - muls
 * @a: int 1
 * @b: int 2
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divs
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - mods
 * @a: int 1
 * @b: int 2
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
